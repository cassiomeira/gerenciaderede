async function main() {
  try {
    const nodesResp = await fetch(`http://localhost:3001/api/maps/${mapId}`);
    const mapData = await nodesResp.json();
    const dbNodes = mapData.nodes;

    console.log('--- REORGANIZING ---');

    for (const [ip, coords] of Object.entries(positions)) {
      const node = dbNodes.find(n => n.apIp === ip);
      if (node) {
        await prisma.mapNode.update({
          where: { id: node.id },
          data: { x: coords[0], y: coords[1] }
        });
        console.log(`Updated position: ${ip}`);
      }
    }

    // Limpar links antigos do mapa
    await prisma.mapLink.deleteMany({ where: { mapId } });
    console.log('Old links cleared.');

    // Criar novos links
    for (const link of links) {
      const src = dbNodes.find(n => n.apIp === link.s);
      const tgt = dbNodes.find(n => n.apIp === link.t);

      if (src && tgt) {
        await prisma.mapLink.create({
          data: {
            id: crypto.randomUUID(),
            mapId,
            sourceNodeId: src.id,
            targetNodeId: tgt.id,
            linkType: link.type
          }
        });
        console.log(`Created link: ${link.s} -> ${link.t} (${link.type})`);
      } else {
        console.warn(`Could not create link: Source or Target not found for ${link.s} -> ${link.t}`);
      }
    }

    console.log('--- DONE ---');
  } catch (e) {
    console.error(e);
  } finally {
    await prisma.$disconnect();
  }
}

main();
